void WorkWithIntegers()
{
    // Example 1 
    int a = 18;
    int b = 6;
    int c = a + b;

    Console.WriteLine(c);
    // subtraction
    c = a - b;
    Console.WriteLine(c);

    // multiplication
    c = a * b;
    Console.WriteLine(c);

    // division
    c = a / b;
    Console.WriteLine(c);

    /// Addition, Subtraction, and Multiplication 
    c = a + b - 12 * 17;
    Console.WriteLine(c); 
}
